package BusinessLayer;

public class Ingrediente {
    private int id;
    private String nome;
    private double ncal;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getNcal() {
        return ncal;
    }

    public void setNcal(double ncal) {
        this.ncal = ncal;
    }
}
